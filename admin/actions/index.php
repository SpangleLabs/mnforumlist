<h1>Actions</h1>
Available actions:

<h3><a href="action.killforum1.php">Kill forum</a></h3>
Sets a forums status to dead, using their most recent post count.

<h3><a href="action.killnation1.php">Kill nation</a></h3>
Sets a nation's status to dead and set's all fora to latest post count.

<h3><a href="action.addforum1.php">Add forum</a></h3>
Adds a new forum, adding it to the forum table and updating the data table.

<h3><a href="action.addnation1.php">Add nation</a></h3>
Adds a new nation and related forums and websites and updating the data, forums and websites table.

<h3><a href="action.addcategory1.php">Add category/tag</a></h3>
Adds a new category/tag.

<h3><a href="action.tagnation1.php">Add nation to category/tag nation</a></h3>
Adds a nation to a category/Tags the nation.

<h3><a href="action.addwiki1.php">Add wiki</a></h3>
Adds a new wiki, for which articles can be added.

<h3><a href="action.addwikiarticle1.php">Add wiki article</a></h3>
Adds a new wiki article for a nation.

<h3><a href="action.revivenation1.php">Revive a nation</a></h3>
Revives a nation that is set to dead, asks which forums want to be revived.

<h3><a href="action.killflag1.php">Kill flag</a></h3>
Removes a flag and it's thumbnail and if it is the main flag, unsets it as main flag.

<h3><a href="action.killculture1.php">Kill cultural item</a></h3>
Removes a cultural item's file and it's record in the database.

<h3><a href="action.editculture1.php">Edit cultural item</a></h3>
Edits the comment for a cultural item.

<h3><a href="action.addwebsite1.php">Add website</a></h3>
Adds a new website, updating website and data tables.

<h3><a href="action.changemainflag1.php">Change main flag</a></h3>
Changes the main flag of a nation.